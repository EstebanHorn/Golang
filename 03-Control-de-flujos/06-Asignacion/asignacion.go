package main

import "fmt"

func main() {

	a := 2

	fmt.Println(a)

	//Suma en asignacion
	a += 2
	fmt.Println(a)

	//Resta en asignacion
	a -= 2
	fmt.Println(a)

	//Multiplicaion en asignacion
	a *= 2
	fmt.Println(a)

	//Divicion en asignacion
	a /= 2
	fmt.Println(a)

	//Divicion en asignacion
	a %= 2
	fmt.Println(a)
}
